DECLARE
  CURSOR C_EMPLOYEES
  IS
  SELECT FIRST_NAME, LAST_NAME
  FROM EMPLOYEES;

  TYPE NDT_LIST
  IS
  TABLE OF C_EMPLOYEES%ROWTYPE;

  NAME_LIST NDT_LIST := NDT_LIST(); --new NdtList(); --gıbı gıbı

  COUNTER INTEGER := 0;
BEGIN
  FOR N IN C_EMPLOYEES
  LOOP
    COUNTER := COUNTER + 1;
    NAME_LIST.EXTEND;
    NAME_LIST(COUNTER).FIRST_NAME := N.FIRST_NAME;
    PRINT('EMPLOYEES: ' || NAME_LIST(COUNTER).FIRST_NAME);
  END LOOP;
END;
