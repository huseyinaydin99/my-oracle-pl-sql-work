CREATE TABLE KURSLAR(
  KURS_ID NUMBER(3),
  KURS_ADI VARCHAR2(30),
  EGITMEN VARCHAR2(50),
  BASLANGIC_TARIHI DATE,
  BITIS_TARIHI DATE
);

TRUNCATE TABLE KURSLAR;

DECLARE
  REC_KURS KURSLAR%ROWTYPE;
BEGIN
  REC_KURS.KURS_ID := NVL(REC_KURS.KURS_ID, 0) + 1;
  REC_KURS.EGITMEN := 'HUSEYIN AYDIN';
  REC_KURS.BASLANGIC_TARIHI := TRUNC(SYSDATE);
  REC_KURS.BITIS_TARIHI := REC_KURS.BASLANGIC_TARIHI + 4;

  LOOP
    REC_KURS.KURS_ID := NVL(REC_KURS.KURS_ID, 0) + 1;

    CONTINUE WHEN REC_KURS.KURS_ID = 2;
    EXIT WHEN REC_KURS.KURS_ID = 4;

    /*
    SELECT DECODE(REC_KURS.KURS_ID,
    1, 'JAVA ILE NESNE TABANLI PROGRAMLAMA',
    2, 'PL/SQL',
    3, 'C#')
    INTO REC_KURS.KURS_ADI
    FROM DUAL;
    */

    CASE REC_KURS.KURS_ID
      WHEN 1 THEN REC_KURS.KURS_ADI := 'JAVA OOP';
      WHEN 2 THEN REC_KURS.KURS_ADI := 'DEVOPS';
      WHEN 3 THEN REC_KURS.KURS_ADI := 'C++';
    END CASE;
    INSERT
    INTO KURSLAR(
      KURS_ID, KURS_ADI, EGITMEN, BASLANGIC_TARIHI, BITIS_TARIHI
    )
    VALUES(REC_KURS.KURS_ID, REC_KURS.KURS_ADI, REC_KURS.EGITMEN, REC_KURS.BASLANGIC_TARIHI, REC_KURS.BITIS_TARIHI);

    REC_KURS.BASLANGIC_TARIHI := REC_KURS.BITIS_TARIHI + 1;
    REC_KURS.BITIS_TARIHI := REC_KURS.BASLANGIC_TARIHI + 4;
  END LOOP;
  COMMIT;
END;
