DECLARE
  CURSOR C_EMP
  IS
  SELECT EMPLOYEE_ID, LAST_NAME
  FROM EMPLOYEES
  ORDER BY EMPLOYEE_ID;

  WEMPLOYEE_ID EMPLOYEES.EMPLOYEE_ID%TYPE;
  WLAST_NAME EMPLOYEES.LAST_NAME%TYPE;
BEGIN
  OPEN C_EMP;
  LOOP
    FETCH C_EMP
    INTO WEMPLOYEE_ID, WLAST_NAME;
    EXIT WHEN C_EMP%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE('SIRA NO: ' || C_EMP%ROWCOUNT || ' SICIL NO: ' || WEMPLOYEE_ID || ' SOYADI: ' || WLAST_NAME);
  END LOOP;
  CLOSE C_EMP;
END;
