DROP TABLE EMPLOYEES2;

CREATE TABLE EMPLOYEES2
AS
SELECT * FROM EMPLOYEES;


DECLARE
  REC_EMP EMPLOYEES2 %ROWTYPE;
BEGIN
  SELECT *
  INTO REC_EMP
  FROM EMPLOYEES2
  WHERE EMPLOYEE_ID = 100;

  IF SQL%FOUND THEN
    DBMS_OUTPUT.PUT_LINE('KAYIT VAR 1. KAYIT SAYISI : ' || SQL%ROWCOUNT);
  ELSE
    DBMS_OUTPUT.PUT_LINE('KAYIT YOK 1.');
  END IF;

  IF SQL%NOTFOUND THEN
    DBMS_OUTPUT.PUT_LINE('KAYIT YOK 2.');
  ELSE
    DBMS_OUTPUT.PUT_LINE('KAYIT VAR 2.');
  END IF;

  EXCEPTION WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('KAYIT YOK ISTISNA FIRLATILDI EX: NO_DATA_FOUND');
END;
