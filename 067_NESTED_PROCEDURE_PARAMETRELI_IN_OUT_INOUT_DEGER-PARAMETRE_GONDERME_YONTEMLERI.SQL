    P_NEW_SALARY IN OUT EMPLOYEES.SALARY%TYPE
  )
  IS
  BEGIN
    SELECT SALARY
    INTO P_SALARY
    FROM EMPLOYEES
    WHERE EMPLOYEE_ID = P_ID;

