DECLARE
  CURSOR C_EMP
  IS
  SELECT *
  FROM EMPLOYEES
  ORDER BY EMPLOYEE_ID;
BEGIN
  FOR R_OKU IN C_EMP LOOP
    DBMS_OUTPUT.PUT_LINE('SIRA NO: ' || C_EMP%ROWCOUNT || ' SICIL NO: ' || R_OKU.EMPLOYEE_ID || ' ADI: ' || R_OKU.FIRST_NAME || ' SOYADI: ' || R_OKU.LAST_NAME);
  END LOOP;
END;
