DECLARE
  CURSOR C_EMP
  IS
  SELECT *
  FROM EMPLOYEES
  ORDER BY EMPLOYEE_ID;

  -- WEMPLOYEE_ID EMPLOYEES.EMPLOYEE_ID%TYPE;
  -- WLAST_NAME EMPLOYEES.LAST_NAME%TYPE;

  R_OKU C_EMP%ROWTYPE;
BEGIN
  IF NOT C_EMP%ISOPEN THEN
    OPEN C_EMP;
  END IF;

  LOOP
    FETCH C_EMP
    INTO R_OKU;
    -- EXIT WHEN C_EMP%NOTFOUND;
    EXIT WHEN NOT C_EMP%FOUND OR C_EMP%ROWCOUNT > 10;
    DBMS_OUTPUT.PUT_LINE('SIRA NO: ' || C_EMP%ROWCOUNT || ' SICIL NO: ' || R_OKU.EMPLOYEE_ID || ' ADI: ' || R_OKU.FIRST_NAME || ' SOYADI: ' || R_OKU.LAST_NAME);
  END LOOP;

  IF C_EMP%ISOPEN THEN
    CLOSE C_EMP;
  END IF;
END;
