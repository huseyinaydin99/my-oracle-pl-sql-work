DECLARE
  CURSOR C_LOC
  IS
  SELECT LOCATION_ID, CITY
  FROM LOCATIONS
  ORDER BY CITY;

  R_LOC; C_LOC%ROWTYPE;
BEGIN
    OPEN C_LOC;
    LOOP
      FETCH C_LOC
      INTO R_LOC;
      EXIT WHEN C_LOC%NOTFOUND;
      DBMS_OUTPUT.PUT_LINE(' | LOKASYON: ' || R_LOC.CITY || ' | LOKASYON ID: (' || R_LOC.LOCATION_ID || ')');
    END LOOP;
    CLOSE C_LOC;
END;
