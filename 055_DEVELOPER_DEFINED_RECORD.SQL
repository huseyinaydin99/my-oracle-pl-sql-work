 DECLARE
  TYPE R_ZAMAN_TYPE
  IS
  RECORD(
    CURR_DATE DATE,
    CURR_DAY VARCHAR2(12),
    CURR_TIME VARCHAR2(8) := '00:00:00'
  );

  V_ZAMAN R_ZAMAN_TYPE;
 BEGIN
  SELECT SYSDATE
  INTO V_ZAMAN.CURR_DATE
  FROM DUAL;

  V_ZAMAN.CURR_DAY := TO_CHAR(V_ZAMAN.CURR_DATE, 'DAY');
  V_ZAMAN.CURR_TIME := TO_CHAR(V_ZAMAN.CURR_DATE, 'HH24:MI:SS');

  PRINT('TARIH : ' || TO_CHAR(V_ZAMAN.CURR_DATE, 'DD/MM/YYYY'));
  PRINT('GUN : ' || TO_CHAR(V_ZAMAN.CURR_DAY));
  PRINT('ZAMAN : ' || TO_CHAR(V_ZAMAN.CURR_TIME));

 END;
