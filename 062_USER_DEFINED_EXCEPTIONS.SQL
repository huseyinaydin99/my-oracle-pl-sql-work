DECLARE
  V_DEPT_ID     EMPLOYEES.DEPARTMENT_ID%TYPE;
  V_TOPLAM      NUMBER;
  E_INVALID_ID  EXCEPTION;
BEGIN
  V_DEPT_ID := &DEPT_ID;

  IF V_DEPT_ID > 0 THEN
    SELECT COUNT(*)
    INTO V_TOPLAM
    FROM EMPLOYEES
    WHERE DEPARTMENT_ID = V_DEPT_ID;
    PRINT('TOPLAM: ' || V_TOPLAM);
  ELSE
    RAISE E_INVALID_ID;
  END IF;

  EXCEPTION
  WHEN E_INVALID_ID
  THEN
    PRINT('BOLUM NO NEGATÄ°F OLAMAZ');
END;
