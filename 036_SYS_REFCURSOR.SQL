DECLARE
  WIMLEC SYS_REFCURSOR;
  WQUERY VARCHAR2(1000);
  WESKI_MAAS EMPLOYEES.SALARY%TYPE;
  WYENI_MAAS EMPLOYEES.SALARY%TYPE;
BEGIN
  WQUERY := 'SELECT SALARY, SALARY * 1.30
  FROM EMPLOYEES
  WHERE DEPARTMENT_ID = 100';

  OPEN WIMLEC FOR WQUERY;

  LOOP
    FETCH WIMLEC
    INTO WESKI_MAAS, WYENI_MAAS;
    EXIT WHEN WIMLEC%NOTFOUND;
    PRINT('ESKI MAAS: ' || WESKI_MAAS || ' - ' || WYENI_MAAS);
  END LOOP;

  CLOSE WIMLEC;
END;
