DECLARE
  TYPE NDT_TYPE
  IS
  TABLE OF INTEGER;

  VAR1 NDT_TYPE := NDT_TYPE(10, 20, 30);

  I INTEGER;

  PROCEDURE WRITE
  IS
    I INTEGER;
  BEGIN
    I := VAR1.FIRST;
    WHILE I IS NOT NULL
    LOOP
      PRINT('DEGER: ' || VAR1(I));
      I := VAR1.NEXT(I);
    END LOOP;
    PRINT(' ');
  END WRITE;
BEGIN
  WRITE();
  VAR1.EXTEND(2, 1); --<< SONA 2 TANE HUCRE EKLER AMA 1. HUCREDEKI DEGERLERI UZERINE EKLER SON EKLEDIKLERININ.
  WRITE;

  VAR1.DELETE(5);
  WRITE();

  VAR1.EXTEND(2); --<< DIZI SONUNA 2 TANE NULL HUCRE EKLER.

  VAR1.EXTEND; --<<DIZI SONUNA NULL HUCRE EKLER.
  WRITE;
END;
