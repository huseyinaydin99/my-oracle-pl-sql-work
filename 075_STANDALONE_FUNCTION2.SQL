    VAL INTEGER := 0;
    USER_INPUT_ INTEGER := &GIR;
BEGIN
    VAL := FAKTORIEL(USER_INPUT_);
    PRINT(USER_INPUT_ || ' DEGERININ FAKTORIELI: ' || VAL);
END;


