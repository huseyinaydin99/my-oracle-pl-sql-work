
CREATE OR REPLACE
FUNCTION FAKTORIEL(
  N INTEGER
) RETURN INTEGER
IS
  FACT INTEGER;
BEGIN
  IF N = 0 THEN
    FACT := 1;
  ELSIF N = 1 THEN
    FACT := 1;
  ELSIF N < 0 THEN
    FACT := NULL;
  ELSE
    FACT := N * FAKTORIEL(N - 1);
  END IF;

  RETURN(FACT);
END;

SET DEFINE ON;
