DECLARE
  CURSOR C_EMP(WDEPTNO EMPLOYEES.DEPARTMENT_ID%TYPE DEFAULT 90) --<<PARAMETRE NE GIRERSEN ELIYNEN O GELIR SENIYNEN (-:) VARSAYILAN 90'DIR.
  IS
  SELECT WDEPTNO, EMPLOYEE_ID, LAST_NAME, DEPARTMENT_ID
  FROM EMPLOYEES
  WHERE DEPARTMENT_ID = WDEPTNO;

  R_EMP C_EMP%ROWTYPE;
BEGIN
  OPEN C_EMP; --<< PARAMETRE. EGER GIRMEZSEK VARSAYILANI YUKARDA TANIMLADIK 90'DIR.
    LOOP
      FETCH C_EMP
      INTO R_EMP;
      EXIT WHEN C_EMP%NOTFOUND;
      DBMS_OUTPUT.PUT_LINE('GELEN PARAMETRE: ' || R_EMP.WDEPTNO || '|SICIL NO: ' || R_EMP.EMPLOYEE_ID || ' | SOYADI: ' || R_EMP.LAST_NAME || ' | DEPARTMAN ID:' || R_EMP.DEPARTMENT_ID || '|');
    END LOOP;
  CLOSE C_EMP;
END;
/

SET SERVEROUTPUT ON;
