DECLARE
  DOGUM_TARIHI DATE := TO_DATE('11/09/2005', 'dd/mm/yyyy');
  YASI NUMBER(3);
  CIKTI VARCHAR2(500);
BEGIN
  YASI := (SYSDATE - DOGUM_TARIHI) / 365;

  --SEARCHED CASE
  CASE
    WHEN YASI < 15 THEN
      -- CIKTI := 'BEN BIR COCUGUM YASIM : ' || YASI;
      DBMS_OUTPUT.PUT_LINE('BEN BIR COCUGUM YASIM : ' || YASI);
    WHEN YASI < 20 THEN
      DBMS_OUTPUT.PUT_LINE('BEN GENCIM : ' || YASI);
      CASE
        WHEN YASI > 18 THEN
          CIKTI := 'BEN 19 YASINDAYIM HACI ABI';
      END CASE;
    WHEN YASI < 30 THEN
      DBMS_OUTPUT.PUT_LINE('YIRMILI YASLARDAYIM : ' || YASI);
    WHEN YASI < 40 THEN
      DBMS_OUTPUT.PUT_LINE('OTUZLU YASLARDAYIM : ' || YASI);
    WHEN YASI < 50 THEN
      DBMS_OUTPUT.PUT_LINE('KIRKLI YASLARDAYIM : ' || YASI);
    ELSE
      DBMS_OUTPUT.PUT_LINE('BIR AYAGIM CUKURDA : ' || YASI);
  END CASE;
  DBMS_OUTPUT.PUT_LINE(CIKTI);
END;

