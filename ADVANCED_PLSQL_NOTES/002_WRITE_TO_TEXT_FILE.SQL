DECLARE
    TXTDOSYA UTL_FILE.FILE_TYPE;
    TXTDOSYA2 UTL_FILE.FILE_TYPE;
    PARAGRAF VARCHAR2(2000);
    I NUMBER := 0;
BEGIN
    TXTDOSYA := UTL_FILE.FOPEN('TEXT_DIR', 'DOSYA.txt', 'R');
    TXTDOSYA2 := UTL_FILE.FOPEN('TEXT_DIR', 'DOSYA2.txt', 'W');
    LOOP
    I := I + 1;
        BEGIN
            UTL_FILE.GET_LINE(TXTDOSYA, PARAGRAF);
            UTL_FILE.PUT_LINE(TXTDOSYA2, I || '. SATIR: ' || PARAGRAF);
            EXCEPTION WHEN NO_DATA_FOUND THEN EXIT;
        END;
    END LOOP;
    UTL_FILE.FCLOSE(TXTDOSYA);
    UTL_FILE.FCLOSE(TXTDOSYA2);
END;