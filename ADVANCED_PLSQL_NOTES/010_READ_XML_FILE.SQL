SELECT *
FROM EMP;

SELECT X.GETCLOBVAL()
FROM EMP_XML X;

-- XML TABLODAN VERI OKUYUP DEGERLERI DISARI AKTARMAK
SELECT EXTRACT(VALUE(D), '//EMPNO/text()').GETNUMBERVAL() AS EMPNO,
       EXTRACT(VALUE(D), '//ENAME/text()').GETSTRINGVAL() AS ENAME,
       EXTRACT(VALUE(D), '//SAL/text()').GETNUMBERVAL() AS SAL,
       VALUE(D) DEGER
FROM EMP_XML X,
     TABLE(
        XMLSEQUENCE(
            EXTRACT(VALUE(X), '/ROWSET/EMP')
            )
     ) D;