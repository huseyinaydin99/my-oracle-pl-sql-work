DECLARE
    TXT_DOSYA UTL_FILE.FILE_TYPE;
    PARAGRAF VARCHAR2(2000);
BEGIN
    TXT_DOSYA := UTL_FILE.FOPEN('TEXT_DIR', 'DOSYA.txt', 'R');
    LOOP
        BEGIN
            UTL_FILE.GET_LINE(TXT_DOSYA, PARAGRAF);
            PRINT(PARAGRAF);
            EXCEPTION WHEN NO_DATA_FOUND THEN EXIT;
        END;
    END LOOP;
    UTL_FILE.FCLOSE(TXT_DOSYA);
END;

SET SERVEROUTPUT ON;