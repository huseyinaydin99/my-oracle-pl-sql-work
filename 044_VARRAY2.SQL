DECLARE
  TYPE TAKIMLAR
  IS
  VARRAY(10)
  OF VARCHAR(30);

  TAKIM TAKIMLAR := TAKIMLAR('BJK', 'GS', 'TS', 'FB'); --<< NEWLENDI
BEGIN
    PRINT('2002');
    PRINT('____--____--____');

    FOR I IN 1..TAKIM.COUNT
    LOOP
      PRINT(I || '.' || TAKIM(I));
    END LOOP;

    PRINT(' ');
    TAKIM.EXTEND; --<< VARRAY ASLINDA 10 HUCRELI ANCAK HAZIRLAYICI METOTDA 4 TANE GIRILDI ONDAN DOLAYI GENISLETILMESI GEREKIYOR
    TAKIM(5) := 'NIGDE SPOR';

    TAKIM(4) := 'CASGEM SPOR';
    TAKIM(2) := 'FERTEK SPOR';

    FOR I IN 1..TAKIM.COUNT
    LOOP
      PRINT(I || '.' || TAKIM(I));
    END LOOP;

    TAKIMLAR := TAKIMLAR('BJK', 'GS', 'TS', 'FB'); --<< YENIDEN NEWLENDI VE TEKRAR 4 BOYUTA INDIRGENDI.
END;
