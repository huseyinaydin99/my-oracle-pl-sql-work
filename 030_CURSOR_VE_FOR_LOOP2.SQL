DECLARE
  CURSOR C_EMP
  IS
  SELECT *
  FROM EMPLOYEES
  ORDER BY EMPLOYEE_ID;
  INDEX_ NUMBER := 0;
BEGIN
  FOR R_OKU IN
  (SELECT *
  FROM EMPLOYEES
  ORDER BY EMPLOYEE_ID) LOOP
    INDEX_ := INDEX_ + 1;
    DBMS_OUTPUT.PUT_LINE('SIRA NO: ' || INDEX_ || ' SICIL NO: ' || R_OKU.EMPLOYEE_ID || ' ADI: ' || R_OKU.FIRST_NAME || ' SOYADI: ' || R_OKU.LAST_NAME);
  END LOOP;
END;
