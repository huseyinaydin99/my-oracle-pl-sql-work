DECLARE
  CURSOR C_OKU
  IS
  SELECT EMPLOYEE_ID, FIRST_NAME, SALARY
  FROM EMPLOYEES
  WHERE SALARY < (SELECT AVG(SALARY)
                  FROM EMPLOYEES);
  CALISAN C_OKU%ROWTYPE;
BEGIN
  SELECT AVG(SALARY)
  INTO CALISAN.SALARY
  FROM EMPLOYEES;
  PRINT('ORTALAMA MAAS : ' || CALISAN.SALARY);

  OPEN C_OKU;
  LOOP
    FETCH C_OKU
    INTO CALISAN;

    EXIT WHEN C_OKU%NOTFOUND;

    PRINT('CALISAN: ' || CALISAN.EMPLOYEE_ID || ' ' || CALISAN.SALARY);
  END LOOP;
  CLOSE C_OKU;
END;
