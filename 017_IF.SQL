DECLARE
  DOGUM_TARIHI DATE := TO_DATE('11/09/2005', 'dd/mm/yyyy');
  YASI NUMBER(3);
BEGIN
  YASI := (SYSDATE - DOGUM_TARIHI) / 365;

  IF YASI < 15 THEN
    BEGIN
      DBMS_OUTPUT.PUT_LINE('BEN BIR COCUGUM YASIM : ' || YASI);
    END;
  ELSIF YASI < 20 THEN
    DBMS_OUTPUT.PUT_LINE('BEN BIR GENCIM YASIM : ' || YASI);
    IF YASI > 18 THEN
      DBMS_OUTPUT.PUT_LINE('BEN 19 YASINDAYIM');
    END IF;
  ELSIF YASI < 30 THEN
    DBMS_OUTPUT.PUT_LINE('YIRMILI YASLARDAYIM : ' || YASI);
  ELSIF YASI < 40 THEN
    DBMS_OUTPUT.PUT_LINE('OTUZLU YASLARDAYIM : ' || YASI);
  ELSE
    DBMS_OUTPUT.PUT_LINE('BEN BIR COCUK DEGILIM YASIM : ' || YASI);
  END IF;
END;
