DECLARE
 TYPE R_ISIM
 IS
 RECORD(
   AD EMPLOYEES.FIRST_NAME%TYPE,
   SOYAD EMPLOYEES.LAST_NAME%TYPE
 );

 TYPE CONTACT
 IS
 RECORD(
   KISI R_ISIM, --NESTED RECORD,
   TELEFON EMPLOYEES.PHONE_NUMBER%TYPE
 );

 TYPE DIZI
 IS
 TABLE OF CONTACT; --<< NESTED TABLE DATA TYPE WITH RECORD.

 REHBER DIZI := DIZI();
 J NUMBER := 0;
BEGIN
  FOR I IN (SELECT FIRST_NAME, LAST_NAME, PHONE_NUMBER
  FROM EMPLOYEES
  WHERE DEPARTMENT_ID = 60)
  LOOP
    J := J + 1;
    REHBER.EXTEND;
    REHBER(J).KISI.AD := I.FIRST_NAME;
    REHBER(J).KISI.SOYAD := I.LAST_NAME;
    REHBER(J).TELEFON := I.PHONE_NUMBER;
  END LOOP;

  J := REHBER.FIRST; --<< ILK KAYDI ATAR

  WHILE J IS NOT NULL
  LOOP
    PRINT('AD: ' || REHBER(J).KISI.AD || ' SOYAD: ' || REHBER(J).KISI.SOYAD || ' TELEFON: ' || REHBER(J).TELEFON);
    J := REHBER.NEXT(J);
  END LOOP;
END;
