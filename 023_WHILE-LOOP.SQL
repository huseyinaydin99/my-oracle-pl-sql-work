DECLARE
  TARIH1 DATE;
  TARIH2 DATE;
  WBOOL BOOLEAN;
BEGIN
  TARIH1 := TO_DATE('26/08/1071', 'DD/MM/YYYY');
  TARIH2 := LAST_DAY(TARIH1); --<< ILGILI TARIHIN AYININ SON GUNUNU VERIR.
  WBOOL := TARIH1 <= TARIH2;
  WHILE WBOOL LOOP --<< ILGILI TARIHIN AY SONUNA KADAR SAYAR
    DBMS_OUTPUT.PUT_LINE(TO_CHAR(TARIH1, 'DAY') || ' ' || TO_CHAR(TARIH1, 'DD/MM/YYYY'));
    TARIH1 := TARIH1 + 1;
    -- EXIT WHEN TARIH1 > TO_DATE('29/08/1071', 'DD/MM/YYYY'); --<< AYIN 23'UNDA DONGUYU KESER.
    WBOOL := TARIH1 <= TARIH2;
  END LOOP;
  -- DBMS_OUTPUT.PUT_LINE(TO_CHAR(TARIH1, 'DAY') || ' ' || TO_CHAR(TARIH1, 'DD/MM/YYYY'));
END;
