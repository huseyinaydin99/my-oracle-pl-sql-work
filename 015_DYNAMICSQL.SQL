DECLARE
  DDL_KOMUT VARCHAR2(2000);
BEGIN
  DDL_KOMUT :=  'CREATE TABLE TEMP_TABLE(
    URUN_ID NUMBER(10),
    URUN_ADI VARCHAR2(30)
  )';
  DDL_KOMUT := 'ALTER TABLE TEMP_TABLE ADD URUN_FIYATI NUMBER(15, 2)';
  DDL_KOMUT := 'GRANT SELECT ON TEMP_TABLE TO HUSO';
  DDL_KOMUT := 'REVOKE SELECT ON TEMP_TABLE FROM HUSO';
  DDL_KOMUT := 'DROP TABLE TEMP_TABLE';
  EXECUTE IMMEDIATE DDL_KOMUT;
  DBMS_OUTPUT.PUT_LINE('TABLO OLUSTURULDU');
END;


