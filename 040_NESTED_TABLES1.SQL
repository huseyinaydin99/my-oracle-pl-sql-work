DECLARE
  TYPE NDT_ISIMLER
  IS TABLE OF VARCHAR2(10); --<< NESTED DATA TYPELERDE INDEX KISMI BELİRLENMEZ ÇÜNKÜ DAİMA INTEGER'DIR GARDASH.!

  TYPE NDT_PUANLAR
  IS TABLE OF INTEGER;

  ISIMLER NDT_ISIMLER;
  PUANLAR NDT_PUANLAR;
BEGIN
  ISIMLER := NDT_ISIMLER('HUSEYIN', 'USSUUUN', 'AAAMET', 'OGUZ');
  PUANLAR := NDT_PUANLAR(98, 22, 77, 1500);

  PRINT('TOPLAM OGRENCI SAYISI: ' || ISIMLER.COUNT);

  FOR I IN 1..ISIMLER.COUNT
  LOOP
    PRINT('OGRENCI: ' || ISIMLER(I) || 'PUAN: ' || PUANLAR(I));
  END LOOP;
END;
