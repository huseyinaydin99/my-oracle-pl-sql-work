DECLARE
  CALISANLAR EMPLOYEES%ROWTYPE;
BEGIN
  SELECT *
  INTO CALISANLAR
  FROM EMPLOYEES
  WHERE SALARY = (SELECT MAX(SALARY)
  FROM EMPLOYEES);

  PRINT('ISIM : ' || CALISANLAR.FIRST_NAME);
  PRINT('SOYISMI : ' || CALISANLAR.LAST_NAME);
  PRINT('MAAS : ' || CALISANLAR.SALARY);
END;
