BEGIN
  FOR SAYAC IN 1..5 LOOP
    DBMS_OUTPUT.PUT_LINE('SAYAC: ' || SAYAC);
  END LOOP;
END;

BEGIN
  FOR SAYAC IN REVERSE 1..5 LOOP
    EXIT WHEN SAYAC = 3;
    --CONTINUE WHEN SAYAC = 3;
    DBMS_OUTPUT.PUT_LINE('SAYAC: ' || SAYAC);
  END LOOP;
END;

DECLARE
  FAKTORYEL NUMBER := 1;
BEGIN
  FOR SAYAC IN 1..5 LOOP
    FAKTORYEL := FAKTORYEL * SAYAC;
  END FOR;
  DBMS_OUTPUT.PUT_LINE('5 != ' || FAKTORYEL);
END;

SET SERVEROUTPUT ON;
